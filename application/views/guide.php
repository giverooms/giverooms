			<p>You can exchange your dollars into Myanmar Kyat when you get to your hotel or guesthouse, in a local bank or at the airport – the latter not always being not as good a rate as the others. Refrain from using the money exchangers on the street who try to lure you with higher rates – it is said they cleverly short change you and unless you are willing to count your money three times, then it’s not worth the hassle.</p>

